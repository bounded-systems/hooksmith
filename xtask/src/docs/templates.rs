//! Template-based documentation generation
//!
//! Provides template-based documentation generation using simple string templates.

use anyhow::{Context, Result};
use std::collections::HashMap;
use std::fs;
use std::path::Path;

/// Template context for variable substitution
pub type TemplateContext = HashMap<String, String>;

/// Generate documentation from template
pub fn generate_from_template(template_name: &str, context: &TemplateContext) -> Result<String> {
    let template_content = load_template(template_name)?;
    let rendered = render_template(&template_content, context)?;
    Ok(rendered)
}

/// Load template from templates directory
fn load_template(template_name: &str) -> Result<String> {
    let template_path = Path::new("templates").join(format!("{}.hbs", template_name));

    if template_path.exists() {
        fs::read_to_string(&template_path)
            .context(format!("Failed to read template: {:?}", template_path))
    } else {
        // Return a default template if the file doesn't exist
        Ok(get_default_template(template_name))
    }
}

