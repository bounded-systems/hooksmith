/// Render template with context
fn render_template(template: &str, context: &TemplateContext) -> Result<String> {
    let mut rendered = template.to_string();

    // Simple variable substitution: {{variable_name}}
    for (key, value) in context {
        let placeholder = format!("{{{{{}}}}}", key);
        rendered = rendered.replace(&placeholder, value);
    }

    // Handle simple loops: {{#each items}}...{{/each}}
    rendered = handle_simple_loops(&rendered, context)?;

    Ok(rendered)
}

/// Handle simple loops in templates
fn handle_simple_loops(template: &str, context: &TemplateContext) -> Result<String> {
    let mut result = template.to_string();

    // Find loop patterns: {{#each items}}...{{/each}}
    let loop_pattern = regex::Regex::new(r"\{\{#each\s+(\w+)\}\}(.*?)\{\{/each\}\}")
        .context("Failed to compile loop regex")?;

    for cap in loop_pattern.captures_iter(template) {
        let loop_var = &cap[1];
        let loop_content = &cap[2];

        if let Some(items) = context.get(loop_var) {
            // For now, we'll just replace with a placeholder
            // In a full implementation, this would parse the items and iterate
            let replacement = format!("<!-- Loop for {}: {} -->", loop_var, items);
            result = result.replace(&cap[0], &replacement);
        }
    }

    Ok(result)
}

