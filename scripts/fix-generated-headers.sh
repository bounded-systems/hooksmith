# Colors for output
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m' # No Color

echo -e "${BLUE}🔧 Fixing generated headers for Hooksmith project...${NC}"

# Function to add header to a file
add_header() {
    local file="$1"
    local header_type="$2"
    
    if [[ ! -f "$file" ]]; then
        echo -e "${YELLOW}⚠️  File not found: $file${NC}"
        return 1
    fi
    
    # Check if header already exists
    if grep -q "@generated by xtask" "$file" 2>/dev/null; then
        echo -e "${YELLOW}⚠️  Header already exists in: $file${NC}"
        return 0
    fi
    
    # Create temporary file
    local temp_file=$(mktemp)
    
    case "$header_type" in
        "markdown"|"md")
            cat > "$temp_file" << 'EOF'
<!-- @generated by xtask gen-docs -->

EOF
            ;;
        "yaml"|"yml")
            cat > "$temp_file" << 'EOF'
# @generated by xtask gen-config

EOF
            ;;
        "toml")
            cat > "$temp_file" << 'EOF'
# @generated by xtask gen-config

EOF
            ;;
        "json"|"jsonc")
            cat > "$temp_file" << 'EOF'
// @generated by xtask gen-schema

EOF
            ;;
        "wit")
            cat > "$temp_file" << 'EOF'
;; @generated by xtask gen-wit

EOF
            ;;
        "html"|"css")
            cat > "$temp_file" << 'EOF'
<!-- @generated by xtask gen-docs -->

EOF
            ;;
        "hbs")
            cat > "$temp_file" << 'EOF'
{{!-- @generated by xtask gen-templates --}}

EOF
            ;;
        "dot")
            cat > "$temp_file" << 'EOF'
// @generated by xtask gen-diagrams

EOF
            ;;
        *)
            cat > "$temp_file" << 'EOF'
# @generated by xtask gen-config

EOF
            ;;
    esac
    
    # Add original content
    cat "$file" >> "$temp_file"
    
    # Replace original file
    mv "$temp_file" "$file"
    
    echo -e "${GREEN}✅ Added header to: $file${NC}"
}

# Function to get file extension
get_extension() {
    local file="$1"
    local ext="${file##*.}"
    if [[ "$ext" == "$file" ]]; then
        echo ""
    else
        echo "$ext"
    fi
}

